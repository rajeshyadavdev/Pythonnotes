"""
Python Assignment operators are used to assign values to variables. Following is a table which shows all Python assignment operators.
"""
# = assigning value to variables
a = 10
print("Value of a:",a)#10

# += adding then assigning value to variable
b = 10
b += 5
print("Value of b:",b)#15

# -= subtracting then assigning value to variable
c = 10
c -= 5
print("Value of c:",c)#5


# *= multiplying then assigning value to variable
d = 10
d *= 5
print("Value of d:",d)#50

# /= dividing then assigning value to variable
e = 10
e /= 5
print("Value of e:",e)#2.0

# %= modulus dividing then assigning value to variable
f = 10
f %= 5
print("Value of f:",f)#0 remainder

# **= exponential then assigning value to variable
g = 2
g **= 3
print("Value of g:",g)#8 

# //= floor division then assigning value to variable
h = 5
h //= 2
print("Value of h:",h)#2

