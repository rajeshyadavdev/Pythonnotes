"""
Python Arithmetic operators are used to perform basic mathematical operations such as addition, subtraction, multiplication, etc.
"""


a = 20 
b = 5

# Addition (+) 
add = a+b
print(f"Addition of {a} and {b} = {add}")#25

#Subtraction(-)
sub = a-b
print(f"Subtraction of {a} and {b} = {sub}")#15

#Multiplication(*)
mul = a*b
print(f"Multiplication of {a} and {b} = {mul}")#100

#Division(/)
div = a/b
print(f"Division of {a} and {b} = {div}")#4.9 we get float type

#Modulus(%)
mod = a%b
print(f"Modulus of {a} and {b} = {mod}")#0 remainder will be answer


# Exponents(**)
x = 2
y = 4

exponent = x**y
print(f"Exponents of {x} and {y} = {exponent}")

#Floor Division
divident = 5
divisor = 2
floor_div = divident // divisor
print(f"Floor division of {divident} and {divisor} = {floor_div}") #2






